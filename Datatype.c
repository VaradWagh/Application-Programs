#include<stdio.h>

int main()
{
	int iNo1 = 10;				// 4  	
	
	float fValue = 3.14;			//4
	
	double dData = 6.10;				//8
		
	char cValue = 'M';						//1
	
	printf("Integer is %d\n",iNo1);
	printf("Float is %f\n",fValue);
	printf("Double is %lf\n",dData);
	printf("Char is %c\n",cValue);
	
	return 0;
}
